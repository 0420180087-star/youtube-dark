import { VideoMetadata } from "../types";

/**
 * YouTube Video Upload Service — Complete rewrite.
 *
 * Uses the YouTube Data API v3 Resumable Upload protocol:
 * 1. POST metadata → get resumable upload URI
 * 2. PUT video bytes → get video ID
 * 3. POST thumbnail (optional)
 *
 * All errors are caught with clear, actionable Portuguese messages.
 */
export const uploadVideoToYouTube = async (
    accessToken: string,
    videoFile: File,
    metadata: VideoMetadata,
    thumbnailUrl?: string,
    scheduledDate?: string,
    onProgress?: (percent: number) => void
): Promise<string> => {

    if (!accessToken) throw new Error("Token de acesso ausente. Reconecte seu canal do YouTube em Configurações.");
    if (!videoFile || videoFile.size === 0) throw new Error("Arquivo de vídeo inválido ou vazio.");

    // ── 1. Build metadata ────────────────────────────
    let title = (metadata?.youtubeTitle || "Untitled Video").substring(0, 100);
    let description = (metadata?.youtubeDescription || "Generated by DarkStream AI").substring(0, 5000);

    if (metadata?.isShorts) {
        if (!title.toLowerCase().includes('#shorts')) title = `${title.substring(0, 92)} #Shorts`.trim();
        if (!description.toLowerCase().includes('#shorts')) description = `${description}\n\n#Shorts`.trim();
    }

    const privacyStatus = scheduledDate ? "private" : (metadata?.visibility || "private");

    const body: any = {
        snippet: {
            title,
            description,
            tags: metadata?.tags || [],
            categoryId: metadata?.categoryId || "24",
        },
        status: {
            privacyStatus,
            selfDeclaredMadeForKids: false,
            embeddable: true,
        },
    };

    if (scheduledDate) {
        const d = new Date(scheduledDate);
        if (isNaN(d.getTime())) throw new Error("Data de agendamento inválida.");
        body.status.publishAt = d.toISOString();
        body.status.privacyStatus = "private";
    }

    // ── 2. Request resumable upload URI ──────────────
    console.log(`[YouTube] Iniciando upload (${(videoFile.size / 1024 / 1024).toFixed(1)} MB)…`);

    const initRes = await safeFetch(
        `https://www.googleapis.com/upload/youtube/v3/videos?uploadType=resumable&part=snippet,status,contentDetails`,
        {
            method: "POST",
            headers: {
                Authorization: `Bearer ${accessToken}`,
                "Content-Type": "application/json",
                "X-Upload-Content-Length": videoFile.size.toString(),
                "X-Upload-Content-Type": videoFile.type || "video/mp4",
            },
            body: JSON.stringify(body),
        }
    );

    if (!initRes.ok) {
        await handleYouTubeError(initRes, "iniciar upload");
    }

    const uploadUrl = initRes.headers.get("Location");
    if (!uploadUrl) throw new Error("YouTube não retornou URL de upload. Tente novamente.");

    // ── 3. Upload video bytes via XHR (for progress) ─
    console.log("[YouTube] Enviando vídeo…");
    const videoId = await uploadWithProgress(uploadUrl, videoFile, onProgress);
    console.log(`[YouTube] ✅ Vídeo enviado: ${videoId}`);

    // ── 4. Upload thumbnail (best-effort) ────────────
    if (videoId && thumbnailUrl && thumbnailUrl.startsWith("data:")) {
        await uploadThumbnail(accessToken, videoId, thumbnailUrl);
    }

    if (onProgress) onProgress(100);
    return videoId;
};

