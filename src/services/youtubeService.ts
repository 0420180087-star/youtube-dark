import { VideoMetadata } from "../types";

/**
 * YouTube Upload Service v3 — Complete rewrite.
 *
 * Flow:
 * 1. Validate inputs
 * 2. POST metadata → get resumable upload URI
 * 3. PUT video bytes with progress tracking via XHR
 * 4. POST thumbnail (best-effort)
 *
 * All errors are surfaced with clear, actionable Portuguese messages.
 */

// ─────────────────────────────────────────────────────
// PUBLIC API
// ─────────────────────────────────────────────────────

export const uploadVideoToYouTube = async (
    accessToken: string,
    videoFile: File,
    metadata: VideoMetadata,
    thumbnailUrl?: string,
    scheduledDate?: string,
    onProgress?: (percent: number) => void
): Promise<string> => {

    // ── 0. Input validation ──────────────────────────
    if (!accessToken || accessToken.trim().length < 10) {
        throw new Error("Token de acesso ausente ou inválido. Vá em Configurações → desconecte e reconecte seu canal do YouTube.");
    }
    if (!videoFile || videoFile.size === 0) {
        throw new Error("Arquivo de vídeo inválido ou vazio. Verifique se o vídeo foi renderizado corretamente.");
    }
    if (videoFile.size > 128 * 1024 * 1024 * 1024) {
        throw new Error("Arquivo excede o limite de 128 GB do YouTube.");
    }

    // ── 1. Verify token is still valid ───────────────
    console.log("[YouTube] Verificando token de acesso…");
    await verifyAccessToken(accessToken);

    // ── 2. Build metadata body ───────────────────────
    let title = (metadata?.youtubeTitle || "Untitled Video").substring(0, 100);
    let description = (metadata?.youtubeDescription || "Generated by DarkStream AI").substring(0, 5000);

    if (metadata?.isShorts) {
        if (!title.toLowerCase().includes('#shorts')) title = `${title.substring(0, 92)} #Shorts`.trim();
        if (!description.toLowerCase().includes('#shorts')) description = `${description}\n\n#Shorts`.trim();
    }

    const privacyStatus = scheduledDate ? "private" : (metadata?.visibility || "private");

    const body: Record<string, any> = {
        snippet: {
            title,
            description,
            tags: (metadata?.tags || []).slice(0, 500), // YouTube max 500 tags
            categoryId: metadata?.categoryId || "24",
        },
        status: {
            privacyStatus,
            selfDeclaredMadeForKids: false,
            embeddable: true,
        },
    };

    if (scheduledDate) {
        const d = new Date(scheduledDate);
        if (isNaN(d.getTime())) throw new Error("Data de agendamento inválida. Use o formato correto.");
        if (d.getTime() < Date.now()) throw new Error("A data de agendamento deve ser no futuro.");
        body.status.publishAt = d.toISOString();
        body.status.privacyStatus = "private"; // YouTube requires private for scheduling
    }

    // ── 3. Request resumable upload URI ──────────────
    const fileSizeMB = (videoFile.size / 1024 / 1024).toFixed(1);
    console.log(`[YouTube] Iniciando upload resumável (${fileSizeMB} MB, tipo: ${videoFile.type || 'video/mp4'})…`);
    if (onProgress) onProgress(2);

    const initRes = await safeFetch(
        `https://www.googleapis.com/upload/youtube/v3/videos?uploadType=resumable&part=snippet,status`,
        {
            method: "POST",
            headers: {
                Authorization: `Bearer ${accessToken}`,
                "Content-Type": "application/json; charset=UTF-8",
                "X-Upload-Content-Length": videoFile.size.toString(),
                "X-Upload-Content-Type": videoFile.type || "video/mp4",
            },
            body: JSON.stringify(body),
        }
    );

    if (!initRes.ok) {
        await handleYouTubeError(initRes, "iniciar upload");
    }

    const uploadUrl = initRes.headers.get("Location");
    if (!uploadUrl) {
        throw new Error("YouTube não retornou URL de upload resumável. Tente novamente em alguns segundos.");
    }

    console.log("[YouTube] URL de upload obtida. Enviando vídeo…");
    if (onProgress) onProgress(5);

    // ── 4. Upload video bytes via XHR (for progress) ─
    const videoId = await uploadWithProgress(uploadUrl, videoFile, accessToken, onProgress);
    console.log(`[YouTube] ✅ Vídeo enviado com sucesso! ID: ${videoId}`);

    // ── 5. Upload thumbnail (best-effort) ────────────
    if (videoId && thumbnailUrl) {
        if (onProgress) onProgress(92);
        await uploadThumbnail(accessToken, videoId, thumbnailUrl);
    }

    // ── 6. Wait for YouTube to finish processing ─────
    if (videoId) {
        if (onProgress) onProgress(95);
        console.log('[YouTube] Aguardando processamento do YouTube...');
        try {
            await waitForProcessing(accessToken, videoId, onProgress);
        } catch (e: any) {
            // Non-fatal: video was uploaded, just log the processing issue
            console.warn('[YouTube] Aviso de processamento:', e.message);
        }
    }

    if (onProgress) onProgress(100);
    return videoId;
};

/**
 * Quick check: can we hit YouTube API with this token?
 */
export const verifyYouTubeToken = async (token: string): Promise<boolean> => {
    try {
        const res = await fetch(
            "https://www.googleapis.com/youtube/v3/channels?part=id&mine=true",
            { headers: { Authorization: `Bearer ${token}` } }
        );
        return res.ok;
    } catch {
        return false;
    }
};

